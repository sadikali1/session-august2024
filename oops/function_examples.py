
def get_square(item_list):
    square = []
    for value in item_list:
        square.append(value ** 2)
    return square


list = [10, 20, 30, 40, 50]

a = get_square(list)
print(a)

list1 = [2, 3, 4, 5, 6, 7]
print(get_square(list1))

list2 = [12, 13, 14, 15, 16, 17]
print(get_square(list2))


